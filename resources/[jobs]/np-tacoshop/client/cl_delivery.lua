AddEventHandler("np-tacoshop:deliveryStation", function(pParameters)
    if pParameters.stationId == 1 then
        if rusticosotemideiasmerdasesseserverezap > 0 then return end
        rusticosotemideiasmerdasesseserverezap = 10

        TriggerEvent("player:receiveItem", "delivery_taco", rusticosotemideiasmerdasesseserverezap)
        TriggerEvent("np-tacoshop:deliveryStart")
    elseif pParameters.stationId == 2 then
        TriggerEvent("np-tacoshop:deliveryEnd")
    end
end)

AddEventHandler("np-tacoshop:deliveryStart", function()
	local location = deliveryLocations[math.random(#deliveryLocations)]

    CreateBlip(location)

    TriggerEvent("DoLongHudText", "New delivery in " .. location.street .. ", was marked on your GPS!")

    while true do
		Citizen.Wait(1)

        local plyCoords = GetEntityCoords(PlayerPedId())
		local distance = #(plyCoords - location["pos"]["xyz"])

		if distance < 25.0 then
			ShowFloatingHelpNotification("~INPUT_FRONTEND_RB~ delivery point", location["pos"]["xyz"])

			if IsControlJustReleased(0, 38) and distance < 2.0 then
				break
			end
		end

        if rusticosotemideiasmerdasesseserverezap == 0 then
            Citizen.Wait(1000)
            break
        end
	end

    DeleteBlip()

    local hasItem = exports["np-inventory"]:hasEnoughOfItem("delivery_taco", 1, false, true)
    if hasItem then
        TriggerEvent("animation:PlayAnimation", "knock")

        Citizen.Wait(4000)

        TriggerEvent("animation:PlayAnimation", "c")

        TriggerEvent("inventory:removeItem", "delivery_taco", 1)
        TriggerServerEvent("np-heists:complete", math.random(5, 15))

        rusticosotemideiasmerdasesseserverezap = rusticosotemideiasmerdasesseserverezap - 1

        if rusticosotemideiasmerdasesseserverezap > 0 then
            TriggerEvent("np-tacoshop:deliveryStart")
        else
            TriggerEvent("np-tacoshop:deliveryEnd")
        end
    elseif not hasItem and rusticosotemideiasmerdasesseserverezap > 0 then
        TriggerEvent("np-tacoshop:deliveryEnd")
    end
end)

AddEventHandler("np-tacoshop:deliveryEnd", function()
    rusticosotemideiasmerdasesseserverezap = 0

    local deliveryItens = exports["np-inventory"]:getQuantity("delivery_taco")
    if deliveryItens > 0 then
        TriggerEvent("inventory:removeItem", "delivery_taco", deliveryItens)
    end

    DeleteBlip()

    TriggerEvent("DoLongHudText", "Closed deliveries")
end)

